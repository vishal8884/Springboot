package com.BankApp.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.TableGenerator;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

import org.hibernate.annotations.Type;
import org.springframework.format.annotation.DateTimeFormat;

@Entity
public class BankCustomer {
	
	@TableGenerator(name = "Bank_Gen", table = "ID_GEN", pkColumnName = "GEN_NAME", valueColumnName = "GEN_VAL", pkColumnValue = "Addr_Gen", initialValue = 10000, allocationSize = 100)
	  @Id
	  @GeneratedValue(strategy = GenerationType.TABLE, generator = "Bank_Gen")
	private int custId;
	private String firstName;
	private String middleName;
	private String lastName;
	private String city;
	private String mobileNo;
	private String occupation;
	@DateTimeFormat(pattern = "yyyy-MM-dd")
	@Type(type="date")
	private Date dob;
	public int getCustId() {
		return custId;
	}
	public void setCustId(int custId) {
		this.custId = custId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	public String getOccupation() {
		return occupation;
	}
	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}
	public Date getDob() {
		return dob;
	}
	public void setDob(Date dob) {
		this.dob = dob;
	}
	@Override
	public String toString() {
		return "BankCustomer [custId=" + custId + ", firstName=" + firstName + ", middleName=" + middleName
				+ ", lastName=" + lastName + ", city=" + city + ", mobileNo=" + mobileNo + ", occupation=" + occupation
				+ ", dob=" + dob + "]";
	}
	
	


}
